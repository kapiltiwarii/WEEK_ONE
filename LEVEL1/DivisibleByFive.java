import java.util.*;

public class DivisibleByFive {
      public static void main(String[] args) {
            Scanner sc = new Scanner(System.in);
            int number = sc.nextInt();

            if (number % 5 == 0) {
                  System.out.println("Yes the " + number + " is devisible by 5");
            } else {
                  System.out.println("Yes the " + number + " is devisible by 5");
            }
      }
}